public class JavaDivision {
    public static void main(String args[])
    {
        int x = 10;
        System.out.printf("%d\n", x / 2); // 5
        System.out.printf("%d\n", x / 3); // 3
        System.out.printf("%d\n", x / 4); // 2
        System.out.printf("%d\n", x / 5); // 2
        System.out.printf("%d\n", x / 6); // 1
        System.out.printf("%d\n", x / 11); // 0
    }
}
