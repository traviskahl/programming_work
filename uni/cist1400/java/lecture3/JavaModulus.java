public class JavaModulus {
    public static void main(String args[])
    {
        int x = 10;
        System.out.printf("%d\n", x % 2);
        System.out.printf("%d\n", x % 3);
        System.out.printf("%d\n", x % 4);
        System.out.printf("%d\n", x % 5);
        System.out.printf("%d\n", x % 6);
        System.out.printf("%d\n", x % 11);
    }
}
